import java.lang.*;
import java.io.*;
import java.util.*;
import java.math.*;

public class Circle{
    public static void main(String[] args) {
        int a = -5;
        while (a!=0) {
            a++;
        }
        do {
            a++;
        } while (a<5);
        for (int i = 0; i < a; i++) {
            continue;
        }
    }
}